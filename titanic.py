#1データの読み込み　※3つ目は性別のみの仮予想データのため不要
train_data=pd.read_csv("/content/drive/MyDrive/Colab Notebooks/Kaggle/TITANIC/train.csv")
test_data=pd.read_csv("/content/drive/MyDrive/Colab Notebooks/Kaggle/TITANIC/test.csv")
